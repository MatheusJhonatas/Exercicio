/*26) Escreva um programa que exiba os números de 1 a 100 na tela em ordem decrescente.
Meu código:*/

using System;

namespace Program
{
    public class Program
    {
        public static void Main()
        {
            for (var i = 100; i >= 1; i--)
            {
                Console.WriteLine(i);
            }
        }
    }
}


